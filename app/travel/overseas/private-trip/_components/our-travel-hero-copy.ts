/** `[태그]` 또는 `[태그/태그]` 형태를 제거하고 앞뒤 공백 정리 */
export function stripOurTravelHeroBracketTags(text: string): string {
  return text
    .replace(/\[[^\[\]]+\]\s*/g, '')
    .replace(/\s{2,}/g, ' ')
    .trim()
}
